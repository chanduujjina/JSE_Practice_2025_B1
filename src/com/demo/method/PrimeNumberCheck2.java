package com.demo.method;

public class PrimeNumberCheck2 { 
	
	public static void main(String[] args) {
		
		int inputNumber = 25;
		int count = 0;
		for(int i = 0;i<=inputNumber;i++);
		if (inputNumber %1 ==0) {
			count++;
			
		}
		
		if (count ==2 ) {
			System.out.println("Given Number:"+"inputNumber"+"is a Prime Number");
		}
		
	}
}
	
	    
	
	
	
	
package com.demo.method;

public class EvenandOddNumber2 {
	
	public static void main(String[] args) {
		
		//find the even number from  1 to 100
		
		
		for (int i =1;i<=100;i++) {
			if (i%100 ==0 ) {
				System.out.println("Even Number:" +i);
			}
			else {
				System.out.println("Odd Number:"+i);
			}
				
		}
		
		
		}
		
		
	}

	
	   
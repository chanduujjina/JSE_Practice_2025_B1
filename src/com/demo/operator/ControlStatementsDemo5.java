package com.demo.operator;

public class ControlStatementsDemo5 {
	
	public static void main(String[] args) {
		
		String name = "Test";
		
		if (name == "Sabitha") {
			System.out.println("name:"+name);
		}
		
		else if (name == "Sekhar") {
			System.out.println("name:"+name);
		}
		
		else if (name == "Hemanth") {
			System.out.println("name:"+name);
		}
		
		
		else if (name == "Sudhakar") {
			System.out.println("name:"+name);
		}
		
		else {
			System.out.println("default block:"+name);
		}
		
		
		
		
	}

}

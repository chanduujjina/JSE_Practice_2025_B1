package com.demo;

public class ArithematicDemo1 {
	
	public static void main(String[] args) {
		
		
		int a = 20;// assigment operator
		
		int b = 10;
		
		int addResult = a+b;
		
		System.out.println("addition of a,b :"+ addResult);
		
		int subtractResult = a-b;
		
		System.out.println("subtraction of a,b :"+ subtractResult);
		
		int mulResult = a*b;
		
		System.out.println("Multiplication of a,b :"+ mulResult);
		
		int divisionResult = a/b;
		
		System.out.println("division of a,b :"+ divisionResult);
		
		int remainder = a%b;
		
		System.out.println("remainder for a/b :"+ remainder);
		
	}

}
